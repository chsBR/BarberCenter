    public TelaAgendamento(int idBarbeiro, String nomeBarbeiro) {
        initComponents();
        setLocationRelativeTo(null);

        idBarbeiroSelecionado = idBarbeiro;

        JSpinner.DateEditor editor
                = new JSpinner.DateEditor(seletorData, "dd/MM/yyyy");
        seletorData.setEditor(editor);

        SpinnerDateModel model = (SpinnerDateModel) seletorData.getModel();
        model.setCalendarField(Calendar.DAY_OF_MONTH);

        Calendar hoje = Calendar.getInstance();
        hoje.set(Calendar.HOUR_OF_DAY, 0);
        hoje.set(Calendar.MINUTE, 0);
        hoje.set(Calendar.SECOND, 0);
        hoje.set(Calendar.MILLISECOND, 0);

        model.setStart(hoje.getTime());
        seletorData.setValue(hoje.getTime());

        Calendar c = Calendar.getInstance();
        c.setTime((Date) seletorData.getValue());

        int dia = c.get(Calendar.DAY_OF_WEEK);
        if (dia == Calendar.SATURDAY) {
            c.add(Calendar.DAY_OF_MONTH, 2);
            seletorData.setValue(c.getTime());
        } else if (dia == Calendar.SUNDAY) {
            c.add(Calendar.DAY_OF_MONTH, 1);
            seletorData.setValue(c.getTime());
        }

        seletorData.addChangeListener(e -> {

            if (ajustandoData) {
                return;
            }

            Calendar cal = Calendar.getInstance();
            cal.setTime((Date) seletorData.getValue());

            int diaSemana = cal.get(Calendar.DAY_OF_WEEK);

            if (diaSemana == Calendar.SATURDAY || diaSemana == Calendar.SUNDAY) {

                JOptionPane.showMessageDialog(
                        this,
                        "Não atendemos aos finais de semana."
                );

                ajustandoData = true;

                do {
                    cal.add(Calendar.DAY_OF_MONTH, 1);
                } while (cal.get(Calendar.DAY_OF_WEEK) == Calendar.SATURDAY
                        || cal.get(Calendar.DAY_OF_WEEK) == Calendar.SUNDAY);

                seletorData.setValue(cal.getTime());
                ajustandoData = false;
                return;
            }

            carregarHorarios();
        });

        carregarHorarios();
        carregarServicos();
    }

