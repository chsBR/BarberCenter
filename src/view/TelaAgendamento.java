    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        int linha = tblHorarios.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um horário.");
            return;
        }

        String status = tblHorarios.getValueAt(linha, 1).toString();

        if (!status.equalsIgnoreCase("Disponível")) {
            JOptionPane.showMessageDialog(this, "Este horário não está disponível.");
            return;
        }

        int indice = cbServicos.getSelectedIndex();

        if (indice < 0 || indice >= listaServicos.size()) {
            JOptionPane.showMessageDialog(this, "Selecione um serviço.");
            return;
        }

        int idCliente = Sessao.getCliente().getId();
        int idBarbeiro = idBarbeiroSelecionado;

        Servico servicoSelecionado = listaServicos.get(indice);
        int idServico = servicoSelecionado.getId();

        String horarioSelecionado = tblHorarios.getValueAt(linha, 0).toString();

        java.util.Date data = (java.util.Date) seletorData.getValue();
        java.sql.Date dataSql = new java.sql.Date(data.getTime());

        Timestamp dataHora = Timestamp.valueOf(
                dataSql.toString() + " " + horarioSelecionado
        );

        AgendamentoDAO dao = new AgendamentoDAO();

        if (dao.salvarAgendamento(idCliente, idBarbeiro, idServico, dataHora)) {
            JOptionPane.showMessageDialog(this, "Agendamento realizado com sucesso!");
            TelaMenu tela = new TelaMenu();
            tela.setLocationRelativeTo(null);
            tela.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar agendamento.");
        }
    }//GEN-LAST:event_btnAgendarActionPerformed

