    private void carregarHorarios() {
        DefaultTableModel model
                = (DefaultTableModel) tblHorarios.getModel();

        model.setRowCount(0);

        String[] horarios = {
            "09:00:00", "10:00:00", "11:00:00",
            "13:00:00", "14:00:00", "15:00:00",
            "16:00:00", "17:00:00"
        };

        java.util.Date utilDate = (java.util.Date) seletorData.getValue();
        java.sql.Date dataSelecionada = new java.sql.Date(utilDate.getTime());

        AgendamentoDAO dao = new AgendamentoDAO();
        List<String> ocupados
                = dao.listarHorariosOcupados(idBarbeiroSelecionado, dataSelecionada);

        for (String h : horarios) {
            String status = ocupados.contains(h)
                    ? "Ocupado"
                    : "Disponível";

            model.addRow(new Object[]{h, status});
        }
    }

    private void carregarServicos() {
        cbServicos.removeAllItems();
        listaServicos.clear();

        ServicoDAO dao = new ServicoDAO();
        List<Servico> servicos = dao.listarTodos();

        for (Servico s : servicos) {
            listaServicos.add(s);
            cbServicos.addItem(s.getNome());
        }
    }

