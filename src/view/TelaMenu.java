    private void carregarBarbeiros() {
        DefaultTableModel model = (DefaultTableModel) tblBarbeiros.getModel();
        model.setRowCount(0);

        BarbeiroDAO dao = new BarbeiroDAO();

        for (Object[] b : dao.listar()) {
            double media = (double) b[2];
            int total = (int) b[3];

            model.addRow(new Object[]{
                b[0], // id
                b[1], // nome
                media == 0 ? "Sem avaliações" : "⭐ " + media,
                total
            });
        }
    }

    private void carregarEstrelas() {
        int idCliente = Sessao.getCliente().getId();

        FidelidadeDAO dao = new FidelidadeDAO();
        int total = dao.buscarTotalEstrelas(idCliente);

        lblEstrelas.setText("⭐ " + total);
    }

