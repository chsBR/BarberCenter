    private void btnNovoAgendamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoAgendamentoActionPerformed
        int linhaSelecionada = tblBarbeiros.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Selecione um barbeiro para continuar.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int idBarbeiro = (int) tblBarbeiros.getValueAt(linhaSelecionada, 0);
        String nomeBarbeiro = tblBarbeiros.getValueAt(linhaSelecionada, 1).toString();

        TelaAgendamento tela = new TelaAgendamento(idBarbeiro, nomeBarbeiro);
        tela.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnNovoAgendamentoActionPerformed

