    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!validarRegistro()) {
            return;
        }

        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String senha = String.valueOf(txtSenha.getPassword()).trim();

        Cliente cliente = new Cliente(
                0,
                nome,
                email,
                senha,
                TipoUsuario.CLIENTE
        );

        ClienteDAO dao = new ClienteDAO();

        if (dao.cadastrar(cliente)) {
            JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");

            TelaLogin tela = new TelaLogin();
            tela.setLocationRelativeTo(null);
            tela.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro ao cadastrar usuário.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    /**
     * Valida os dados informados na tela de registro.
     *
     * @return true se os dados forem válidos, false caso contrário
     */
    private boolean validarRegistro() {
        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String senha = String.valueOf(txtSenha.getPassword()).trim();

        if (nome.isEmpty() || email.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
            return false;
        }

        if (nome.length() < 2 || !nome.matches("[A-Za-zÀ-ÿ ]+")) {
            JOptionPane.showMessageDialog(this, "Nome inválido.");
            return false;
        }

        if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            JOptionPane.showMessageDialog(this, "E-mail inválido.");
            return false;
        }

        if (senha.length() < 6) {
            JOptionPane.showMessageDialog(this, "A senha deve ter pelo menos 6 caracteres.");
            return false;
        }

        return true;
    }

