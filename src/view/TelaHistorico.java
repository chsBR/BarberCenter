    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed
        int linha = tblHistorico.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um agendamento.");
            return;
        }

        String status = tblHistorico.getValueAt(linha, 3).toString();

        if (!status.equalsIgnoreCase("Agendado")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Apenas agendamentos em aberto podem ser concluídos."
            );
            return;
        }

        int confirmar = JOptionPane.showConfirmDialog(
                this,
                "Deseja marcar este agendamento como concluído?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmar != JOptionPane.YES_OPTION) {
            return;
        }

        int idAgendamento = idsAgendamentos.get(linha);

        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
        ServicoDAO servicoDAO = new ServicoDAO();
        FidelidadeDAO fidelidadeDAO = new FidelidadeDAO();
        HistoricoPagamentoDAO pagamentoDAO = new HistoricoPagamentoDAO();

        int idCliente = agendamentoDAO.buscarIdClientePorAgendamento(idAgendamento);
        int idServico = agendamentoDAO.buscarIdServicoPorAgendamento(idAgendamento);

        double valorServico = servicoDAO.buscarValorPorId(idServico);
        int estrelasNecessarias = (int) Math.ceil(valorServico / 5.0);

        String[] opcoes = {
            "Dinheiro",
            "Cartão",
            "Pix",
            "Estrelas (Fidelidade)"
        };

        String formaPagamento = (String) JOptionPane.showInputDialog(
                this,
                "Selecione a forma de pagamento:",
                "Pagamento",
                JOptionPane.QUESTION_MESSAGE,
                null,
                opcoes,
                opcoes[0]
        );

        if (formaPagamento == null) {
            JOptionPane.showMessageDialog(
                    this,
                    "Pagamento cancelado. O agendamento não foi concluído."
            );
            return;
        }

        if (formaPagamento.equals("Estrelas (Fidelidade)")) {

            int saldo = fidelidadeDAO.getEstrelas(idCliente);

            if (saldo < estrelasNecessarias) {
                JOptionPane.showMessageDialog(
                        this,
                        "Estrelas insuficientes.\n"
                        + "Necessárias: " + estrelasNecessarias
                        + "\nDisponíveis: " + saldo
                );
                return;
            }

            int confirmarEstrelas = JOptionPane.showConfirmDialog(
                    this,
                    "Este serviço custa " + estrelasNecessarias + " estrelas.\n"
                    + "Deseja confirmar o pagamento?",
                    "Pagamento por Fidelidade",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmarEstrelas != JOptionPane.YES_OPTION) {
                return;
            }

            fidelidadeDAO.debitarEstrelas(idCliente, estrelasNecessarias);
        }

        pagamentoDAO.salvar(idAgendamento, formaPagamento);
        agendamentoDAO.concluirAgendamento(idAgendamento);
        fidelidadeDAO.adicionarEstrela(idCliente);

        carregarHistorico();

        JOptionPane.showMessageDialog(
                this,
                "Agendamento concluído e pagamento registrado com sucesso."
        );
    }//GEN-LAST:event_btnConcluirActionPerformed

    private void btnAvaliarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvaliarActionPerformed
        int linha = tblHistorico.getSelectedRow();
        if (linha == -1) {
            return;
        }

        int idAgendamento = idsAgendamentos.get(linha);

        AvaliacaoDAO dao = new AvaliacaoDAO();
        if (dao.existeAvaliacaoParaAgendamento(idAgendamento)) {
            JOptionPane.showMessageDialog(
                    this,
                    "Este agendamento já foi avaliado."
            );
            atualizarEstadoBotaoAvaliar(false);
            return;
        }

        JComboBox<Integer> cbNota = new JComboBox<>(
                new Integer[]{1, 2, 3, 4, 5}
        );
        JTextArea txtComentario = new JTextArea(5, 20);
        JScrollPane scroll = new JScrollPane(txtComentario);

        Object[] mensagem = {
            "Nota (1 a 5):", cbNota,
            "Comentário (opcional):", scroll
        };

        int opcao = JOptionPane.showConfirmDialog(
                this,
                mensagem,
                "Avaliar Atendimento",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE
        );

        if (opcao != JOptionPane.OK_OPTION) {
            return;
        }

        int nota = (Integer) cbNota.getSelectedItem();
        String comentario = txtComentario.getText().trim();

        dao.salvarAvaliacao(idAgendamento, nota, comentario);

        JOptionPane.showMessageDialog(
                this,
                "Avaliação registrada com sucesso!"
        );
    }//GEN-LAST:event_btnAvaliarActionPerformed

    /**
     * usado para alterar cor durante disable do botao avaliar
     *
     * @param ativo
     */
    private void atualizarEstadoBotaoAvaliar(boolean ativo) {
        btnAvaliar.setEnabled(ativo);

        if (ativo) {
            btnAvaliar.setBackground(corAvaliarAtivo);
            btnAvaliar.setForeground(Color.WHITE);
            btnAvaliar.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
            btnAvaliar.setToolTipText("Avaliar atendimento");
        } else {
            btnAvaliar.setBackground(corAvaliarInativo);
            btnAvaliar.setForeground(Color.DARK_GRAY);
            btnAvaliar.setCursor(Cursor.getDefaultCursor());
            btnAvaliar.setToolTipText(
                    "Apenas agendamentos concluídos podem ser avaliados"
            );
        }
    }

    /**
     * Responsável por carregar dados da tabela de histórico de agendamentos.
     */
    private void carregarHistorico() {
        DefaultTableModel model = (DefaultTableModel) tblHistorico.getModel();
        model.setRowCount(0);

        AgendamentoDAO dao = new AgendamentoDAO();
        idsAgendamentos.clear();

        if (Sessao.isCliente()) {

            int idCliente = Sessao.getCliente().getId();

            for (Object[] linha : dao.listarPorCliente(idCliente)) {

                idsAgendamentos.add((Integer) linha[0]);

                model.addRow(new Object[]{
                    linha[1],
                    linha[2],
                    linha[3],
                    linha[4]
                });
            }

        } else if (Sessao.isBarbeiro()) {

            int idBarbeiro = Sessao.getBarbeiro().getId();

            for (Object[] linha : dao.listarPorBarbeiro(idBarbeiro)) {

                idsAgendamentos.add((Integer) linha[0]);

                model.addRow(new Object[]{
                    linha[1],
                    linha[2],
                    linha[3],
                    linha[4]
                });
            }
        } else if (Sessao.isGerente()) {
            for (Object[] linha : dao.listarParaGerente(0)) {
                idsAgendamentos.add((Integer) linha[0]);
                model.addRow(new Object[]{
                    linha[1],
                    linha[2],
                    linha[3],
                    linha[4]
                });
            }
        }
    }

