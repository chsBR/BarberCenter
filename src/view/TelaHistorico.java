    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        int linha = tblHistorico.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um agendamento.");
            return;
        }

        String status = tblHistorico.getValueAt(linha, 3).toString();

        if (status.equalsIgnoreCase("Cancelado")) {
            JOptionPane.showMessageDialog(this, "Este agendamento já está cancelado.");
            return;
        }

        int opcao = JOptionPane.showConfirmDialog(
                this,
                "Deseja realmente cancelar este agendamento?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {
            int idAgendamento = idsAgendamentos.get(linha);

            AgendamentoDAO dao = new AgendamentoDAO();
            dao.cancelarAgendamento(idAgendamento);

            carregarHistorico();
            JOptionPane.showMessageDialog(this, "Agendamento cancelado com sucesso.");
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

