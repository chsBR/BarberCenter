    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!validarRegistro()) {
            return;
        }

        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String senha = txtSenha.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String cpf = txtCpf.getText().trim();

        Cliente cliente = new Cliente(0, nome, email, senha, TipoUsuario.BARBEIRO);

        ClienteDAO clienteDAO = new ClienteDAO();
        int idCliente = clienteDAO.cadastrarBarbeiro(cliente);

        if (idCliente == -1) {
            JOptionPane.showMessageDialog(this, "Erro ao criar usuário.");
            return;
        }

        BarbeiroDAO barbeiroDAO = new BarbeiroDAO();
        if (!barbeiroDAO.cadastrar(idCliente, telefone, cpf)) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar barbeiro.");
            return;
        }

        JOptionPane.showMessageDialog(this, "Barbeiro cadastrado com sucesso!");
        dispose();
    }//GEN-LAST:event_btnRegistrarActionPerformed

