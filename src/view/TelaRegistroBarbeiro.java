    private void centralizarPainel(JPanel painel) {
        int larguraJanela = getWidth();
        int alturaJanela = getHeight();

        int larguraPainel = painel.getWidth();
        int alturaPainel = painel.getHeight();

        int x = (larguraJanela - larguraPainel) / 2;
        int y = (alturaJanela - alturaPainel) / 2;

        painel.setLocation(x, y);
    }

