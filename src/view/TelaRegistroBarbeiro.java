    /**
     * Valida os dados informados na tela de registro.
     *
     * @return true se os dados forem válidos, false caso contrário
     */
    private boolean validarRegistro() {
        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String senha = String.valueOf(txtSenha.getPassword()).trim();
        String telefone = txtTelefone.getText().trim();
        String cpf = txtCpf.getText().trim();

        if (nome.isEmpty() || email.isEmpty() || senha.isEmpty() || telefone.isEmpty() || cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
            return false;
        }

        if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            JOptionPane.showMessageDialog(this, "E-mail inválido.");
            return false;
        }

        if (senha.length() < 6) {
            JOptionPane.showMessageDialog(this, "A senha deve ter pelo menos 6 caracteres.");
            return false;
        }

        if (!telefone.matches("\\d{10,11}")) {
            JOptionPane.showMessageDialog(this, "Telefone inválido.");
            return false;
        }

        if (!cpf.matches("\\d{11}")) {
            JOptionPane.showMessageDialog(this, "CPF inválido.");
            return false;
        }

        return true;
    }

